# input() –¥–∞–µ—Ç –Ω–∞ –≤—ã—Ö–æ–¥–µ —Å—Ç—Ä–æ–∫—É, –ø–æ—ç—Ç–æ–º—É —Å –ø–æ–º–æ—â—å—é —Ñ—É–Ω–∫—Ü–∏–∏ int()
# –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å –≤–≤–µ–¥–µ–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É –∫ —á–∏—Å–ª–æ–≤–æ–º—É —Ç–∏–ø—É –¥–∞–Ω–Ω—ã—Ö
